def get_shishen(day_gan, target_gan):
    # 生克关系映射：根据日干与目标干的五行关系，确定十神
    relations = {
        ("木", "木"): ("比肩", "劫财"),
        ("木", "火"): ("食神", "伤官"),
        ("木", "土"): ("偏财", "正财"),
        ("木", "金"): ("七杀", "正官"),
        ("木", "水"): ("偏印", "正印"),

        ("火", "火"): ("比肩", "劫财"),
        ("火", "土"): ("食神", "伤官"),
        ("火", "金"): ("偏财", "正财"),
        ("火", "水"): ("七杀", "正官"),
        ("火", "木"): ("偏印", "正印"),

        ("土", "土"): ("比肩", "劫财"),
        ("土", "金"): ("食神", "伤官"),
        ("土", "水"): ("偏财", "正财"),
        ("土", "木"): ("七杀", "正官"),
        ("土", "火"): ("偏印", "正印"),

        ("金", "金"): ("比肩", "劫财"),
        ("金", "水"): ("食神", "伤官"),
        ("金", "木"): ("偏财", "正财"),
        ("金", "火"): ("七杀", "正官"),
        ("金", "土"): ("偏印", "正印"),

        ("水", "水"): ("比肩", "劫财"),
        ("水", "木"): ("食神", "伤官"),
        ("水", "火"): ("偏财", "正财"),
        ("水", "土"): ("七杀", "正官"),
        ("水", "金"): ("偏印", "正印"),
    }

    w1 = WUXING[day_gan]
    w2 = WUXING[target_gan]
    y1 = YINYANG[day_gan]
    y2 = YINYANG[target_gan]

    pair = relations[(w1, w2)]
    # 阴阳相同取阳干对应的十神（比肩/食神/偏财/七杀/偏印），不同取阴干十神
    return pair[0] if y1 == y2 else pair[1]

